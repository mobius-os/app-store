import { readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const catalogPath = resolve(root, 'catalog.json')
const snapshotsPath = resolve(root, 'manifest-snapshots.js')
const required = ['id', 'name', 'version', 'description', 'entry']
const localAppsRoot = process.env.MOBIUS_LOCAL_APPS_ROOT

function refreshUrl(value, id) {
  const url = new URL(value)
  const parts = url.pathname.split('/').filter(Boolean)
  if (
    url.hostname === 'raw.githubusercontent.com' &&
    parts.length >= 4 &&
    parts[2] === 'main'
  ) {
    // The short raw /main/ form can lag a newly pushed branch tip at some CDN
    // edges. GitHub's ref-qualified route resolves the branch first, then
    // redirects to raw content for that exact ref.
    return new URL(
      `https://github.com/${parts[0]}/${parts[1]}/raw/refs/heads/main/` +
      parts.slice(3).join('/'),
    )
  }
  url.searchParams.set('_mobius_refresh', `${Date.now()}-${id}`)
  return url
}

const catalog = JSON.parse(await readFile(catalogPath, 'utf8'))
if (!Array.isArray(catalog.apps) || catalog.apps.length === 0) {
  throw new Error('catalog.json has no apps')
}

const manifests = await Promise.all(catalog.apps.map(async (entry) => {
  if (localAppsRoot) {
    const repoName = String(entry.repo || '').split('/').filter(Boolean).at(-1)
    if (!repoName) throw new Error(`${entry.id}: catalog entry has no repo`)
    const localPath = resolve(localAppsRoot, repoName, 'mobius.json')
    const manifest = JSON.parse(await readFile(localPath, 'utf8'))
    for (const key of required) {
      if (typeof manifest?.[key] !== 'string' || !manifest[key]) {
        throw new Error(`${entry.id}: local manifest is missing ${key}`)
      }
    }
    return manifest
  }
  // A refresh must observe the current branch tip rather than silently
  // republishing an older snapshot. The durable manifest_url stored in the
  // catalog remains unchanged.
  const manifestUrl = refreshUrl(entry.manifest_url, entry.id)
  const response = await fetch(manifestUrl, { cache: 'no-store' })
  if (!response.ok) {
    throw new Error(`${entry.id}: manifest returned HTTP ${response.status}`)
  }
  const manifest = await response.json()
  for (const key of required) {
    if (typeof manifest?.[key] !== 'string' || !manifest[key]) {
      throw new Error(`${entry.id}: manifest is missing ${key}`)
    }
  }
  return manifest
}))

const snapshots = {}
catalog.apps.forEach((entry, index) => {
  entry.manifest = manifests[index]
  snapshots[entry.id] = manifests[index]
})
catalog.updated = new Date().toISOString().slice(0, 10)

const generated = `// Generated by scripts/refresh-manifest-snapshots.mjs.\n` +
  `// Install/update still uses manifest_url; these are a resilient first-paint floor.\n` +
  `export const MANIFEST_SNAPSHOTS = ${JSON.stringify(snapshots, null, 2)}\n`

await Promise.all([
  writeFile(catalogPath, `${JSON.stringify(catalog, null, 2)}\n`),
  writeFile(snapshotsPath, generated),
])
