import { readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const catalogPath = resolve(root, 'catalog.json')
const snapshotsPath = resolve(root, 'manifest-snapshots.js')
const required = ['id', 'name', 'version', 'description', 'entry']

const catalog = JSON.parse(await readFile(catalogPath, 'utf8'))
if (!Array.isArray(catalog.apps) || catalog.apps.length === 0) {
  throw new Error('catalog.json has no apps')
}

const manifests = await Promise.all(catalog.apps.map(async (entry) => {
  const response = await fetch(entry.manifest_url)
  if (!response.ok) {
    throw new Error(`${entry.id}: manifest returned HTTP ${response.status}`)
  }
  const manifest = await response.json()
  for (const key of required) {
    if (typeof manifest?.[key] !== 'string' || !manifest[key]) {
      throw new Error(`${entry.id}: manifest is missing ${key}`)
    }
  }
  return manifest
}))

const snapshots = {}
catalog.apps.forEach((entry, index) => {
  entry.manifest = manifests[index]
  snapshots[entry.id] = manifests[index]
})
catalog.updated = new Date().toISOString().slice(0, 10)

const generated = `// Generated by scripts/refresh-manifest-snapshots.mjs.\n` +
  `// Install/update still uses manifest_url; these are a resilient first-paint floor.\n` +
  `export const MANIFEST_SNAPSHOTS = ${JSON.stringify(snapshots, null, 2)}\n`

await Promise.all([
  writeFile(catalogPath, `${JSON.stringify(catalog, null, 2)}\n`),
  writeFile(snapshotsPath, generated),
])
